#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
    int x,y ;
    scanf("%d",&x);
    scanf("%d",&y);
    printf("%d", x+y);
    printf(" %d",x-y);
    printf("\n");
    float a,b;
    scanf("%f",&a);
    scanf("%f",&b);
    printf("%.1f",a+b);
    printf(" %.1f",a-b);
    
    return 0;
}
